# Determine the operating system
uname_out="$(uname -s)"

# Check if running on Linux
if [ "$uname_out" == "Linux" ]; then
    # Number of terminal windows to create
    num_windows=999999999999999999999999999999

    # Loop to create terminal windows
    for ((i=1; i<=$num_windows; i++)); do
        x-terminal-emulator -e "bash -c 'echo \"$message\"; exec bash'" &
    done

# Check if running on macOS
elif [ "$uname_out" == "Darwin" ]; then
    # Number of terminal windows to create
    num_windows=999999999999999999999999999

    # Loop to create terminal windows
    for ((i=1; i<=$num_windows; i++)); do
        open -a Terminal.app -n --args bash -c "echo \"$message\"; exec bash"
    done

# Operating system not supported
else
    echo "Unsupported operating system: $uname_out"
    exit 1
fi
